// ─── Font rendering ───────────────────────────────────────────────────────────

void gfx_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg)
{
    struct limine_framebuffer *fb = fb_get();
    if (!fb) return;

    // Only handle printable ASCII
    if ((unsigned char)c < 0x20 || (unsigned char)c > 0x7E)
        c = '?';

    const uint8_t *glyph = font8x16[(unsigned char)c - 0x20];

    for (int row = 0; row < GFX_FONT_H; row++)
    {
        int py = y + row;
        if (py < 0 || (uint64_t)py >= fb->height) continue;

        uint8_t bits = glyph[row];
        for (int col = 0; col < GFX_FONT_W; col++)
        {
            int px = x + col;
            if (px < 0 || (uint64_t)px >= fb->width) continue;
            // MSB = leftmost pixel
            uint32_t colour = (bits & (0x80 >> col)) ? fg : bg;
            fb_putpixel(px, py, colour);
        }
    }
}

void gfx_draw_string(int x, int y, const char *s, uint32_t fg, uint32_t bg)
{
    struct limine_framebuffer *fb = fb_get();
    if (!fb || !s) return;

    int cx = x;
    while (*s)
    {
        if (*s == '\n')
        {
            cx = x;
            y += GFX_FONT_H;
        }
        else
        {
            gfx_draw_char(cx, y, *s, fg, bg);
            cx += GFX_FONT_W;
            // Wrap at screen edge
            if ((uint64_t)cx + GFX_FONT_W > fb->width)
            {
                cx = x;
                y += GFX_FONT_H;
            }
        }
        s++;
        if ((uint64_t)y >= fb->height) break;
    }
}