void gfx_draw_hline(int x, int y, int len, uint32_t colour)
{
    struct limine_framebuffer *fb = fb_get();
    if (!fb || y < 0 || (uint64_t)y >= fb->height) return;
    for (int i = 0; i < len; i++)
    {
        int px = x + i;
        if (px >= 0 && (uint64_t)px < fb->width)
            fb_putpixel(px, y, colour);
    }
}

void gfx_draw_vline(int x, int y, int len, uint32_t colour)
{
    struct limine_framebuffer *fb = fb_get();
    if (!fb || x < 0 || (uint64_t)x >= fb->width) return;
    for (int i = 0; i < len; i++)
    {
        int py = y + i;
        if (py >= 0 && (uint64_t)py < fb->height)
            fb_putpixel(x, py, colour);
    }
}

void gfx_fill_rect(int x, int y, int w, int h, uint32_t colour)
{
    struct limine_framebuffer *fb = fb_get();
    if (!fb) return;

    // Clip to screen
    int x0 = x < 0 ? 0 : x;
    int y0 = y < 0 ? 0 : y;
    int x1 = x + w; if ((uint64_t)x1 > fb->width)  x1 = (int)fb->width;
    int y1 = y + h; if ((uint64_t)y1 > fb->height) y1 = (int)fb->height;

    uint8_t *base = (uint8_t *)fb->address;
    for (int py = y0; py < y1; py++)
    {
        uint32_t *row = (uint32_t *)(base + (uint64_t)py * fb->pitch);
        for (int px = x0; px < x1; px++)
            row[px] = colour;
    }
}

void gfx_draw_rect(int x, int y, int w, int h, uint32_t colour)
{
    gfx_draw_hline(x,         y,         w, colour); // top
    gfx_draw_hline(x,         y + h - 1, w, colour); // bottom
    gfx_draw_vline(x,         y,         h, colour); // left
    gfx_draw_vline(x + w - 1, y,         h, colour); // right
}

