#define FONT_W   8
#define FONT_H   8
#define PADDING  2

static uint64_t cursor_x = PADDING;
static uint64_t cursor_y = PADDING;
static uint64_t cols, rows;
static uint32_t fg, bg;

extern uint64_t fb_width(void);
extern uint64_t fb_height(void);

// Forward declare so terminal.c doesn't need to know fb internals
static uint64_t _width, _height;

void terminal_init(void)
{
    // fb must already be inited
    // We read width/height via fb_putpixel bounds — we need them exposed
    // For now store them via a simple global set at init time
    // (fb_init sets _fb, we piggyback off fb.c's internal)
    // Actually just expose them properly:
    _width  = 0;
    _height = 0;

    // We'll call fb_clear then calculate cols/rows after fb_init sets dimensions
    // These are set by terminal_set_size() called from main
}

void terminal_set_size(uint64_t w, uint64_t h)
{
    _width  = w;
    _height = h;
    cols = (w - PADDING * 2) / FONT_W;
    rows = (h - PADDING * 2) / FONT_H;
    cursor_x = PADDING;
    cursor_y = PADDING;
}

void terminal_set_fg(uint8_t r, uint8_t g, uint8_t b)
{
    fg = fb_colour(r, g, b);
}

void terminal_set_bg(uint8_t r, uint8_t g, uint8_t b)
{
    bg = fb_colour(r, g, b);
    fb_clear(bg);
}

static void draw_char(char c, uint64_t x, uint64_t y)
{
    if (c < 32 || c > 127) c = '?';
    const uint8_t *glyph = font[c - 32];
    for (int row = 0; row < FONT_H; row++) {
        for (int col = 0; col < FONT_W; col++) {
            uint32_t colour = (glyph[row] & (1 << col)) ? fg : bg;
            fb_putpixel(x + col, y + row, colour);
        }
    }
}

static void newline(void)
{
    cursor_x = PADDING;
    cursor_y += FONT_H;
    if (cursor_y + FONT_H > _height - PADDING) {
        // TODO: scroll — for now wrap back to top
        cursor_y = PADDING;
        fb_clear(bg);
    }
}

void terminal_putchar(char c)
{
    if (c == '\n') {
        newline();
        return;
    }
    if (c == '\r') {
        cursor_x = PADDING;
        return;
    }
    if (c == '\b') {
        if (cursor_x > PADDING) {
            cursor_x -= FONT_W;
            draw_char(' ', cursor_x, cursor_y);
        }
        return;
    }
    draw_char(c, cursor_x, cursor_y);
    cursor_x += FONT_W;
    if (cursor_x + FONT_W > _width - PADDING)
        newline();
}

void terminal_puts(const char *s)
{
    while (*s) terminal_putchar(*s++);
}

void terminal_set_size_from_current(void)
{
    // re-use stored dimensions, just reset cursor
    cursor_x = PADDING;
    cursor_y = PADDING;
}